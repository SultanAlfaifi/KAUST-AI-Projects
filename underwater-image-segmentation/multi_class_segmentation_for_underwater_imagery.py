class SegDataset(Dataset):
    def __init__(self, image_dir, mask_dir, start=0, end=None, transform=None, target_transform=None):
        # Sort full lists first
        self.image_paths = sorted(glob.glob(os.path.join(image_dir, "*.jpg")))
        self.mask_paths  = sorted(glob.glob(os.path.join(mask_dir, "*.png")))

        if end is None:
            end = len(self.image_paths)

        # Slice AFTER sorting (this makes train/test split correct)
        self.image_paths = self.image_paths[start:end]
        self.mask_paths  = self.mask_paths[start:end]

        self.transform = transform
        self.target_transform = target_transform

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        image = Image.open(self.image_paths[idx]).convert("RGB")
        mask  = Image.open(self.mask_paths[idx]).convert("L")

        if self.transform:
            image = self.transform(image)

        if self.target_transform:
            mask = self.target_transform(mask)

        # IMPORTANT: SUIM classes are already 0..7
        # mask from PILToTensor is (1,H,W) -> make it (H,W)
        mask = mask.squeeze(0).long()

        return image, mask

