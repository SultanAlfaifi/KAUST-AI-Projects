import segmentation_models_pytorch as smp

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

model = smp.Unet(
    encoder_name="efficientnet-b1",
    encoder_weights="imagenet",
    in_channels=3,
    classes=8
).to(device)

"""## **TASK 3: Training and Validation Loops**
- Define the training and validation loops.
"""

def train_one_epoch(model, dataloader, criterion, optimizer, device):
    model.train()
    total_loss = 0.0
    total_pixels = 0

    for images, masks in tqdm(dataloader, leave=False):
        images = images.to(device)
        masks  = masks.to(device)  # masks: (B,H,W) long

        outputs = model(images)    # outputs: (B,8,H,W) logits
        loss = criterion(outputs, masks)

        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        bs = images.size(0)
        total_loss += loss.item() * bs
        total_pixels += bs

    return total_loss / total_pixels


@torch.no_grad()
def validate(model, dataloader, criterion, device):
    model.eval()
    total_loss = 0.0
    total_pixels = 0

    for images, masks in dataloader:
        images = images.to(device)
        masks  = masks.to(device)

        outputs = model(images)
        loss = criterion(outputs, masks)

        bs = images.size(0)
        total_loss += loss.item() * bs
        total_pixels += bs

    return total_loss / total_pixels

"""## **TASK 4: Running Training**
- Define the loss and the optimizer.
- Train the model.
- Print the training and validation losses.
- Plot loss curve.
"""

criterion = nn.CrossEntropyLoss()
optimizer = optim.AdamW(model.parameters(), lr=0.0001)

num_epochs = 10
train_losses = []
val_losses = []

for epoch in range(num_epochs):
    train_loss = train_one_epoch(model, train_loader, criterion, optimizer, device)
    val_loss   = validate(model, test_loader, criterion, device)

    train_losses.append(train_loss)
    val_losses.append(val_loss)

    print(f"Epoch {epoch+1}/{num_epochs}: Train Loss = {train_loss:.4f}, Val Loss = {val_loss:.4f}")

plt.figure(figsize=(7, 5))
