"""# Part 5: Residual Connection

**Task:**

Now let's see if we can improve our model with a residual connection!

1. **Redefine the CNN model** but add a **residual (skip) connection** from the **2nd convolutional layer** to the **4th convolutional layer**
   - You can use either **summation** OR **concatenation** for the skip connection
   - Note: You might need to use pooling to match the size of the skip connection with the 3rd layer output before concatenation!

2. **Retrain** the model with the residual connection
3. Plot the training and validation losses and training and validation accuracy
"""

class CNNModelResidual(nn.Module):
    def __init__(self):
        super(CNNModelResidual, self).__init__()

        # Convolutional Layers
        self.conv1 = nn.Conv2d(3,   16,  3, padding=1)
        self.conv2 = nn.Conv2d(16,  32,  3, padding=1)
        self.conv3 = nn.Conv2d(32,  64,  3, padding=1)
        self.conv4 = nn.Conv2d(64,  128, 3, padding=1)
        self.conv5 = nn.Conv2d(128, 256, 3, padding=1)

        # BatchNorm (bonus)
        self.bn1 = nn.BatchNorm2d(16)
        self.bn2 = nn.BatchNorm2d(32)
        self.bn3 = nn.BatchNorm2d(64)
        self.bn4 = nn.BatchNorm2d(128)
        self.bn5 = nn.BatchNorm2d(256)

        self.relu = nn.ReLU()
        self.pool = nn.MaxPool2d(2, 2)

        # Skip adapters: (B,32,8,8) -> (B,128,2,2)
        self.skip_pool = nn.MaxPool2d(kernel_size=4, stride=4)  # 8x8 -> 2x2
        self.skip_conv = nn.Conv2d(32, 128, kernel_size=1)       # 32 -> 128

        # Fully connected
        self.fc1 = nn.Linear(256 * 1 * 1, 512)
        self.fc2 = nn.Linear(512, 3)

    def forward(self, x):
        x = self.pool(self.relu(self.bn1(self.conv1(x))))   # (B,16,16,16)
        x2 = self.pool(self.relu(self.bn2(self.conv2(x))))  # (B,32,8,8) save for skip
        x = self.pool(self.relu(self.bn3(self.conv3(x2))))  # (B,64,4,4)
        x4 = self.pool(self.relu(self.bn4(self.conv4(x))))  # (B,128,2,2)

        # Residual SUM from conv2 output to conv4 output
        skip = self.skip_conv(self.skip_pool(x2))           # (B,128,2,2)
        x4 = x4 + skip                                      # SUM

        x = self.pool(self.relu(self.bn5(self.conv5(x4))))  # (B,256,1,1)

        x = x.reshape(x.size(0), -1)
        x = self.relu(self.fc1(x))
        x = self.fc2(x)                                     # logits
        return x

