import segmentation_models_pytorch as smp

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

model = smp.Unet(
    encoder_name="efficientnet-b1",
    encoder_weights="imagenet",
    in_channels=3,
    classes=2  # binary: water vs non-water
).to(device)

print(f"Model loaded on {device}")

"""## Part 6: IoU Metric & Training / Validation Functions

"""

def compute_iou(preds, masks, num_classes=2):
    """Mean IoU over all classes."""
    iou_list = []
    preds = preds.cpu()
    masks = masks.cpu()
    for cls in range(num_classes):
        pred_c = (preds == cls)
        mask_c = (masks == cls)
        intersection = (pred_c & mask_c).sum().float()
        union        = (pred_c | mask_c).sum().float()
        if union == 0:
            iou_list.append(torch.tensor(1.0))
        else:
            iou_list.append(intersection / union)
    return torch.stack(iou_list).mean().item()


def train_one_epoch(model, dataloader, criterion, optimizer, device):
    model.train()
    total_loss = 0.0
    total_iou  = 0.0
    n = 0
    for images, masks in tqdm(dataloader, leave=False):
        images, masks = images.to(device), masks.to(device)
        outputs = model(images)
        loss    = criterion(outputs, masks)
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()
        preds = outputs.argmax(dim=1)
        total_loss += loss.item() * images.size(0)
        total_iou  += compute_iou(preds, masks) * images.size(0)
        n += images.size(0)
    return total_loss / n, total_iou / n


@torch.no_grad()
def validate(model, dataloader, criterion, device):
    model.eval()
    total_loss = 0.0
    total_iou  = 0.0
    n = 0
    for images, masks in dataloader:
        images, masks = images.to(device), masks.to(device)
        outputs = model(images)
        loss    = criterion(outputs, masks)
        preds = outputs.argmax(dim=1)
        total_loss += loss.item() * images.size(0)
        total_iou  += compute_iou(preds, masks) * images.size(0)
        n += images.size(0)
    return total_loss / n, total_iou / n

"""## Part 7: Train the Model

"""

criterion = nn.CrossEntropyLoss()
optimizer = optim.AdamW(model.parameters(), lr=1e-4)

num_epochs = 10
train_losses, val_losses = [], []
train_ious,   val_ious   = [], []

for epoch in range(num_epochs):
    t_loss, t_iou = train_one_epoch(model, train_loader, criterion, optimizer, device)
    v_loss, v_iou = validate(model, val_loader, criterion, device)

    train_losses.append(t_loss);  val_losses.append(v_loss)
    train_ious.append(t_iou);     val_ious.append(v_iou)

    print(f"Epoch {epoch+1}/{num_epochs} | "
          f"Train Loss={t_loss:.4f}  IoU={t_iou:.4f} | "
          f"Val   Loss={v_loss:.4f}  IoU={v_iou:.4f}")

"""## Part 8: Plot Loss & IoU Curves

"""

